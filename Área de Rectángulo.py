print("¡Vamos a calcular el área de un rectángulo!")

# Pedimos al usuario la longitud de la base
# Usamos float() para permitir números con decimales
base = float(input("Introduce la longitud de la base del rectángulo: "))

# Pedimos al usuario la altura
altura = float(input("Introduce la altura del rectángulo: "))

# Calculamos el área (Base x Altura)
area = base * altura

# Mostramos el resultado al usuario
print(f"La base del rectángulo es: {base}")
print(f"La altura del rectángulo es: {altura}")
print(f"El área del rectángulo es: {area}")

print("\n¡Así de fácil es hacer cálculos con Python!")